#!/usr/bin/env python3
"""Parse mem_profile of globals region into a boundary-classified map.

Output is address-only (no LLM-generated function names).
"""

import re
import sys
from collections import defaultdict


def parse(path):
    """Returns dict: addr -> { pc -> { count, values, size } }"""
    writes = defaultdict(lambda: defaultdict(lambda: {'count': 0, 'values': set(), 'size': 0}))

    line_re = re.compile(
        r'pc=0x([0-9a-fA-F]+)\s+addr=0x([0-9a-fA-F]+)\s+'
        r'val=0x([0-9a-fA-F]+)\s+sz=(\d+)'
    )

    with open(path) as f:
        for line in f:
            m = line_re.search(line)
            if not m:
                continue
            pc = int(m.group(1), 16)
            addr = int(m.group(2), 16)
            val = int(m.group(3), 16)
            sz = int(m.group(4))

            writes[addr][pc]['count'] += 1
            writes[addr][pc]['values'].add(val)
            writes[addr][pc]['size'] = sz

    return writes


def main():
    if len(sys.argv) < 2:
        print(f'Usage: {sys.argv[0]} <mem_profile.txt> [output.md]')
        sys.exit(1)

    path = sys.argv[1]
    out_path = sys.argv[2] if len(sys.argv) > 2 else None

    writes = parse(path)

    lines = []
    lines.append('# Globals Region Writer Map (Phase B2)')
    lines.append('')
    lines.append('Generated by `parse_globals_profile.py` from mem_profile capture.')
    lines.append('Address-only output — no LLM-generated names.')
    lines.append('')
    lines.append(f'Total globals written: {len(writes)}')
    total_writes = sum(
        stats['count'] for pcs in writes.values() for stats in pcs.values()
    )
    lines.append(f'Total CPU writes: {total_writes:,}')
    lines.append('')

    lines.append('| Global Addr | Size | Writer PC | Writes | Unique Values | Sample Values |')
    lines.append('|-------------|------|-----------|--------|---------------|---------------|')

    for addr in sorted(writes.keys()):
        pcs = writes[addr]
        for pc in sorted(pcs.keys()):
            stats = pcs[pc]
            count = stats['count']
            sz = stats['size']
            vals = sorted(stats['values'])
            n_unique = len(vals)
            samples = ', '.join(f'0x{v:X}' for v in vals[:5])
            if n_unique > 5:
                samples += f' ... (+{n_unique - 5} more)'
            lines.append(
                f'| 0x{addr:08X} | {sz} | 0x{pc:08X} | {count} | {n_unique} | {samples} |'
            )

    output = '\n'.join(lines) + '\n'

    if out_path:
        with open(out_path, 'w', newline='\n') as f:
            f.write(output)
        print(f'Written to {out_path}')
    else:
        print(output)


if __name__ == '__main__':
    main()
