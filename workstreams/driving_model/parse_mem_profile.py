#!/usr/bin/env python3
"""Parse mem_profile output into car[0] and car[1] writer maps.

Output is address-only (no LLM-generated function names) to prevent analysis bias.
Cross-reference with reimpl/build/daytona.map separately if needed.
"""

import re
import sys
from collections import defaultdict

CAR_BASE = 0x06078900
CAR_STRIDE = 0x268  # 616 bytes

def parse_mem_profile(path):
    """Parse mem_profile log into per-car writer maps.

    Returns dict: car_index -> { offset -> [(pc, value, size, count)] }
    """
    # Raw: collect (pc, offset, value, size) per car
    car_writes = defaultdict(lambda: defaultdict(list))

    line_re = re.compile(
        r'pc=0x([0-9a-fA-F]+)\s+addr=0x([0-9a-fA-F]+)\s+'
        r'val=0x([0-9a-fA-F]+)\s+sz=(\d+)'
    )

    with open(path) as f:
        for line in f:
            m = line_re.search(line)
            if not m:
                continue
            pc = int(m.group(1), 16)
            addr = int(m.group(2), 16)
            val = int(m.group(3), 16)
            sz = int(m.group(4))

            if addr < CAR_BASE:
                continue

            car_index = (addr - CAR_BASE) // CAR_STRIDE
            offset = (addr - CAR_BASE) % CAR_STRIDE

            car_writes[car_index][offset].append((pc, val, sz))

    # Aggregate: per (offset, pc), count occurrences and collect unique values
    result = {}
    for car_idx, offsets in car_writes.items():
        agg = {}
        for offset, writes in offsets.items():
            pc_stats = defaultdict(lambda: {'count': 0, 'values': set(), 'size': 0})
            for pc, val, sz in writes:
                pc_stats[pc]['count'] += 1
                pc_stats[pc]['values'].add(val)
                pc_stats[pc]['size'] = sz
            agg[offset] = pc_stats
        result[car_idx] = agg

    return result


def format_writer_map(car_writes, car_index):
    """Format a single car's writer map as markdown."""
    lines = []
    lines.append(f'## Car[{car_index}] Writer Map')
    lines.append('')
    lines.append(f'Base address: 0x{CAR_BASE + car_index * CAR_STRIDE:08X}')
    lines.append('')
    lines.append('| Offset | Size | Writer PC | Writes | Unique Values | Sample Values |')
    lines.append('|--------|------|-----------|--------|---------------|---------------|')

    offsets = car_writes.get(car_index, {})
    for offset in sorted(offsets.keys()):
        pc_stats = offsets[offset]
        for pc in sorted(pc_stats.keys()):
            stats = pc_stats[pc]
            count = stats['count']
            sz = stats['size']
            vals = sorted(stats['values'])
            n_unique = len(vals)
            # Show up to 5 sample values
            samples = ', '.join(f'0x{v:X}' for v in vals[:5])
            if n_unique > 5:
                samples += f' ... (+{n_unique - 5} more)'
            lines.append(
                f'| +0x{offset:03X} | {sz} | 0x{pc:08X} | {count} | {n_unique} | {samples} |'
            )

    return '\n'.join(lines)


def main():
    if len(sys.argv) < 2:
        print(f'Usage: {sys.argv[0]} <mem_profile.txt> [output.md]')
        sys.exit(1)

    path = sys.argv[1]
    out_path = sys.argv[2] if len(sys.argv) > 2 else None

    car_writes = parse_mem_profile(path)

    lines = []
    lines.append('# Comprehensive Writer Map (Phase B)')
    lines.append('')
    lines.append('Generated by `parse_mem_profile.py` from mem_profile capture.')
    lines.append('Address-only output — no LLM-generated names to prevent analysis bias.')
    lines.append('')
    lines.append(f'Car struct base: 0x{CAR_BASE:08X}, stride: 0x{CAR_STRIDE:X} ({CAR_STRIDE} bytes)')
    lines.append('')

    # Summary
    total_writes = sum(
        sum(stats['count'] for stats in pc_stats.values())
        for offsets in car_writes.values()
        for pc_stats in offsets.values()
    )
    lines.append(f'Total CPU writes captured: {total_writes:,}')
    lines.append(f'DMA writes to car struct: 0 (verified)')
    lines.append('')

    for car_idx in sorted(car_writes.keys()):
        offsets = car_writes[car_idx]
        n_offsets = len(offsets)
        n_writers = len(set(
            pc for pc_stats in offsets.values() for pc in pc_stats.keys()
        ))
        lines.append(f'| Car[{car_idx}] | {n_offsets} offsets | {n_writers} unique writers |')

    lines.append('')

    for car_idx in sorted(car_writes.keys()):
        lines.append(format_writer_map(car_writes, car_idx))
        lines.append('')

    output = '\n'.join(lines) + '\n'

    if out_path:
        with open(out_path, 'w', newline='\n') as f:
            f.write(output)
        print(f'Written to {out_path}')
    else:
        print(output)

    # Print summary to stderr
    for car_idx in sorted(car_writes.keys()):
        offsets = car_writes[car_idx]
        n_offsets = len(offsets)
        n_writers = len(set(
            pc for pc_stats in offsets.values() for pc in pc_stats.keys()
        ))
        n_writes = sum(
            stats['count'] for pc_stats in offsets.values()
            for stats in pc_stats.values()
        )
        print(f'Car[{car_idx}]: {n_offsets} offsets, {n_writers} writers, {n_writes:,} writes',
              file=sys.stderr)


if __name__ == '__main__':
    main()
